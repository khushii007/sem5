#include <stdio.h>

int main() {
    int integerNo = 13;
    float floatNo = 2.234;
    char character = 'Z';
    char string[] = "Hello, world!";
    unsigned int unsignedInteger = 123456;
    printf("Integer: %d\n", integerNo);
    printf("Float: %f\n", floatNo);
    printf("Character: %c\n", character);
    printf("String: %s\n", string);
    printf("Unsigned Integer: %u\n", unsignedInteger);
    return 0;
}